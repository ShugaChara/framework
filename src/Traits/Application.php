     * Application name
     * @return mixed|null
     */
    public function getName()
    {
        return fn()->c()->get('app_name', '');
    }

    /**
     * Application version
     * @return mixed|null
     */
    public function getVersion()
    {
        return fn()->c()->get('app_version', 'v1.0.0');
